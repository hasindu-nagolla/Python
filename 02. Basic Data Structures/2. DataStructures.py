# Data Structures in Python
# ========================================================
# Data Structures in Python is a way of organizing and
# storing data in a computer so that it can be accessed
# and modified efficiently.
# ========================================================

# ========================================================
# Section 1: Lists
# ========================================================
my_list = [1, 2, 3, 4, 5]
print("List:", my_list)

# ========================================================
# Section 2: Tuples
# ========================================================
my_tuple = (1, 2, 3, 4, 5)
print("Tuple:", my_tuple)

# ========================================================
# Section 3: Sets
# ========================================================
my_set = {1, 2, 3, 4, 5}
print("Set:", my_set)

# ========================================================
# Section 4: Dictionaries
# ========================================================
my_dict = {'key1': 'value1', 'key2': 'value2', 'key3': 'value3'}
print("Dictionary:", my_dict)


